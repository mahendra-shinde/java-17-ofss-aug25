package demo1;

public class Main3 {

	public static void main(String[] args) {
		int i =0;
		
		for(i=0;i<10;i++) {
			System.out.println("Number : "+i);
		}
		
		i=0;
		while(i<10) {
			System.out.println("Number : "+i);
			i++;
		}
		i=0;
		do {
			System.out.println("Number: "+i);
		}while(i<10);
	}

}
